def normalize_lang(lang):
    lang = (lang or "tr").lower()[:2]
    return lang if lang in SUPPORTED_LANGS else "tr"


def t(key, lang="tr", **kwargs):
    lang = normalize_lang(lang)
    text = UI.get(lang, UI["tr"]).get(key, UI["tr"].get(key, key))
    if kwargs:
        return text.format(**kwargs)
    return text


def get_page_meta(endpoint, lang="tr"):
    lang = normalize_lang(lang)
    meta = PAGE_META.get(endpoint or "", {}).get(lang)
    if meta:
        return meta
    return PAGE_META.get(endpoint or "", {}).get("tr", {"tag": "", "title": "", "description": ""})


def get_prayer_labels(lang="tr"):
    lang = normalize_lang(lang)
    return PRAYER_LABELS.get(lang, PRAYER_LABELS["tr"])


def format_remaining(hours, minutes, lang="tr"):
    lang = normalize_lang(lang)
    if lang == "ar":
        return f"{hours}س {minutes}د"
    if lang == "en":
        return f"{hours}h {minutes}m"
    return f"{hours}s {minutes}dk"


def get_js_strings(lang="tr"):
    lang = normalize_lang(lang)
    return {key: t(key, lang) for key in JS_KEYS}


def surah_name(number, lang="tr"):
    if lang == "tr":
        return SURAH_NAMES_TR.get(number, f"Sure {number}")
    if lang == "ar":
        return f"سورة {number}"
    return f"Surah {number}"
